<?php

namespace App\Mail;

use Illuminate\Bus\Queueable;
use Illuminate\Mail\Mailable;
use Illuminate\Queue\SerializesModels;

class ContactFormMail extends Mailable
{
    use Queueable, SerializesModels;

    public $data; // Datos que pasas al constructor y estarán disponibles en la vista del correo

    /**
     * Create a new message instance.
     *
     * @param  array  $data
     * @return void
     */
    public function __construct(array $data)
    {
        $this->data = $data;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        return $this->from($this->data['email']) // Puedes ajustar esto según tus necesidades
                    ->subject($this->data['subject'])
                    ->markdown('contact.email'); // Nombre de la vista de correo electrónico
    }
}
